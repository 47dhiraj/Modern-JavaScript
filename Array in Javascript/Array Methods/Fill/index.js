// Fill method in Javascript Array

// Note  : Fill method changes the original array


// Example 1 :
var testArray = [2, 4, 6, 8, 1, 3, 5, 9]

console.log(testArray.fill('Agent 47', 6, 8))           // 6 chai inclusive ho but 8 chai exclusive huncha (because in most of the programming language end range is always exclusive)
console.log(testArray.fill('Agent 47'))                 // kei pani range diyena vani, puraii array ko value lai yo Agent 47 le replace or fill gardincha



